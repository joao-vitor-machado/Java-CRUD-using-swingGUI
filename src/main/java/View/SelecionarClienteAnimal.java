    private void jButton30ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton30ActionPerformed
        
        Calendar data = Calendar.getInstance();
        data.setTime(jDateChooser1.getDate());
        String comentarios = jTextField9.getText();
        int hora = Integer.parseInt((String)jComboBox1.getSelectedItem());
        int minutos = Integer.parseInt((String)jComboBox2.getSelectedItem());
        boolean acabou = false;
        int idTratamento = Controller.getTratamentoSelecionado().getId();
        int idVeterinario = Controller.getVeterinarioSelecionado().getId();
        int idAnimal = Controller.getAnimalSelecionado().getId();

        try{
            ConsultaDAO.getInstance().create(1, data, comentarios, hora, minutos, acabou, idTratamento, idVeterinario, idAnimal);
            ((GenericTableModel)jTable8.getModel()).addListOfItems(ConsultaDAO.getInstance().retrieveByIdTratamento(Controller.getTratamentoSelecionado().getId()));        // TODO add your 
            CardLayout cl = (CardLayout)jPanel3.getLayout();
            cl.show(jPanel3, "consultasExames");
        }catch(Exception e){
            String[] opcoes = {"Ok"};
            JOptionPane.showOptionDialog(null, "Um erro ocorreu. Verifique se os dados foram inseridos corretamente", "Atenção!", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, opcoes, opcoes[0]);
        }
        
    }//GEN-LAST:event_jButton30ActionPerformed

