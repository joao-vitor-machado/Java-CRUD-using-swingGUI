    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        if(jToggleButton1.isSelected()){
            List<Tratamento> tratamentosNotConcluidos = TratamentoDAO.getInstance().retrieveByNotConcluido();
            List<Object> listaFinal = new ArrayList<>();
            for(Tratamento tratamento : tratamentosNotConcluidos){
                if(tratamento.getIdAnimal() == Controller.getAnimalSelecionado().getId()){
                    listaFinal.add(tratamento);
                }
            }
            
            ((GenericTableModel)jTable7.getModel()).addListOfItems(listaFinal);
        
        }else{
            ((GenericTableModel)jTable7.getModel()).addListOfItems(TratamentoDAO.getInstance().retrieveByIdAnimal(Controller.getAnimalSelecionado().getId()));
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

